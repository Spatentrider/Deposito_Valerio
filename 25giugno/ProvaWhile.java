public class ProvaWhile {
    public static void main(String[] args) {
        //esempio ciclo while
        //inizializzazione della variabile di controllo
        int i = 1;

        //ciclo while
        while(i <= 5){
            i++; //incremento della variabile di controllo
            System.out.println(i);
        }
        
    }
    
}
