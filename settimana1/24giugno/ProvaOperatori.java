public class ProvaOperatori {
    public static void main(String[] args) {
    //assegnazione valore
    int x = 10;

    //assegnazione valore e valorizzazione con +=
    int y = 10;
    x += 5;

    //valorizzazione con -=
    x -= 5;

    //valorizzazione con *=
    x *= 5;

    //valorizzazione con /=
    x /= 5;

    //prova addizione
    System.out.println(3+5);
     //prova sottrazione
    System.out.println(3-5);
     //prova moltiplicazione
    System.out.println(3*5);
     //prova divisione
    System.out.println(3/5);
     //prova modulo
    System.out.println(5%4);

     //prova minore
    System.out.println(3<5);
     //prova minore o uguale
    System.out.println(3<=5);
     //prova maggiore
    System.out.println(3>5);
     //prova maggiore o uguale
    System.out.println(3>=5);
     //prova ==
    System.out.println(3==5);
     //prova !=
    System.out.println(3!=5);

     //prova operatore logico &&
    System.out.println(3<5 && 10<50);
    //prova operatore logico ||
    System.out.println(3<5 || 10<50);
    //prova operatore logico !
    System.out.println(!(3<5 && 10<50));



        
    }
   
    
}
